//using switch case to ignore multiple else-if conditions
const month = "mar"

switch (month) {
    case "jan":
        console.log("January");
        break;

    case "feb":
        console.log("February");
        break;

    case "mar":
        console.log("March");
        break;

    case "apr":
        console.log("April");
        break;

    default:
        break;
}